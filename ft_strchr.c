#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t	i;
	char	find;

	i = 0;
	find = (unsigned char)c;
	while (s[i] != '\0')
	{
		if (s[i] == find)
			return ((char *)s + i);
		i++;
	}
	if (s[i] == find)
		return ((char *)s + i);
	return (0);
}
